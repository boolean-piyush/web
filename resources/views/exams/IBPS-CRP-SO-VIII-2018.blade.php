    <ul class="list-group">
        <li class="list-group-item red">1.) Minimum Passing Marks in interview is
    40% (35% for SC/ST/OBC/PWD candidates)</li>
    <br>
    <li class="list-group-item red">2.) The weightage (ratio) of Main Examination and interview
    will be 80:20 respectively</li>
    <br>
