<h3 class="text-center heading"><u>INTELLIGENCE AND PERSONALITY TEST (Interview) Syllabus</u></h3>

<p>The SSB procedure consists of two stage Selection process - stage I and stage II. Only
those candidates who clear the stage I are permitted to appear for stage II. The
details are:- <br><br>
<span><b>(a) Stage I</b></span> comprises of Officer Intelligence Rating (OIR) tests are Picture Perception*
Description Test (PP&DT). The candidates will be shortlisted based on
combination of performance in OIR Test and PP&DT. <br>
<span><b>(b) Stage II</b></span> Comprises of Interview, Group Testing Officer Tasks, Psychology Tests
and the Conference. These tests are conducted over 4 days. The details of these tests
are given on the website www.joinindianarmy.nic.in.</p>

<p>The personality of a candidate is assessed by three different assessors viz. The
Interviewing Officer (IO), Group Testing Officer (GTO) and the Psychologist. There
are no separate weightage for each test. The marks are allotted by assessors only
after taking into consideration the performance of the candidate holistically in all the
test. In addition, marks for Conference are also allotted based on the initial
performance of the Candidate in the three techniques and decision of the Board. All
these have equal weightage.
The various tests of IO, GTO and Psych are designed to bring out the
presence/absence of Officer Like Qualities and their trainability in a candidate.
Accordingly candidates are Recommended or Not Recommended at the SSB.</p> 

